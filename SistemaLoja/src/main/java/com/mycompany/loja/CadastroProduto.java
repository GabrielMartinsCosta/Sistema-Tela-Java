    private void jCriarProdutoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCriarProdutoButtonActionPerformed
        // TODO add your handling code here:
        String tipo = jTipoProdutoText.getText();
        String nome = jNomeProdutoText.getText();
        
        try {
            double preco = Double.parseDouble(jPrecoProdutoText.getText());
            int quantidade = Integer.parseInt(jQuantidadeProdutoText.getText());

            criar(tipo, nome, preco, quantidade);
            int estoqueInicial = Integer.parseInt(jQuantidadeProdutoText.getText());
            estoqueDefinidoProduto.put(nome, estoqueInicial);
            limpar_tela();
        
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, insira um valor válido para o preço e a quantidade.", "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jCriarProdutoButtonActionPerformed

