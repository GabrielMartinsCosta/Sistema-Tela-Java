    private void criar(String tipo, String nome, double preco, int quantidade) {
    boolean produtoExistente = produtos.stream()
            .anyMatch(p -> p.getNomeProduto().equalsIgnoreCase(nome) && p.getTipo().equalsIgnoreCase(tipo));
    
    if (produtoExistente) {
        JOptionPane.showMessageDialog(this, "Já existe um produto cadastrado com o mesmo nome deste, tente novamente.", "Erro", JOptionPane.ERROR_MESSAGE);
    } else {
        Produto novoProduto = new Produto(tipo, nome, quantidade);
        novoProduto.setTipo(tipo);
        novoProduto.setNomeProduto(nome);
        novoProduto.setPreco(preco);
        novoProduto.setEstoque(quantidade);
        produtos.add(novoProduto);
        JOptionPane.showMessageDialog(this, "Produto criado com sucesso!");
    }
    
    }
    
    private void excluir() {
        if (indiceAtual != -1 && indiceAtual < produtos.size()) {
            produtos.remove(indiceAtual);
            limpar_tela();
        } else {
            JOptionPane.showMessageDialog(this, "Não há produtos para excluir.");
    }
        
    }
    
    private void editar(String tipo, String nome, double preco, int quantidade) {
        
        if (indiceAtual != -1 && indiceAtual < produtos.size()) {
            Produto produtoAtual = produtos.get(indiceAtual);
            produtoAtual.setTipo(tipo);
            produtoAtual.setNomeProduto(nome);
            produtoAtual.setPreco(preco);
            produtoAtual.setEstoque(quantidade);
            JOptionPane.showMessageDialog(this, "Produto editado com sucesso!");
            limpar_tela();
        } else {
            JOptionPane.showMessageDialog(this, "Não há produtos para editar.");
    }
        
    }

    private void salvar_tela(Produto produtoSalvar) {
        
    jTipoProdutoText.setText(produtoSalvar.getTipo());
    jNomeProdutoText.setText(produtoSalvar.getNomeProduto());
    jPrecoProdutoText.setText(Double.toString(produtoSalvar.getPreco()));
    jQuantidadeProdutoText.setText(Integer.toString(produtoSalvar.getEstoque()));
    
    }
    
    private void limpar_tela(){

    jTipoProdutoText.setText("");
    jNomeProdutoText.setText("");
    jPrecoProdutoText.setText("");
    jQuantidadeProdutoText.setText("");
    it=produtos.iterator();
    indice=0;
    
    }
    
    

